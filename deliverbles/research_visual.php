            <p><strong>How to make objects more attractive?</strong></p>
            <p>An article by Babich (2021) describes different ways to add more visual weight to UI objects. Below are the ways that are relevant to the portfolio design:</p>
            <ol>
            <li>Size: Larger objects attract the eye more than smaller ones.</li>
            <li>Shape: Objects with a regular shape appear heavier than objects with an irregular shape.</li>
            <li>Orientation: vertical objects appear heavier than horizontal objects.</li>
            <li>Colour: some colours are heavier than others. Saturated colours will gain more attention than unsaturated ones.</li>
            <li>Contrast: When an element contrasts with its surroundings will appear visually heavier.</li>
            <li>Texture: Elements with texture are perceived as heavier in comparison with non-textured elements.</li>
            <li>Whitespace: the more whitespace near a certain object, the more attention it receives.</li>
            <li>Z-depth: Elements in the foreground have more weight than elements in the background.</li>
            <li>Human faces: Eyes and faces are considered as objects with heavy visual weight. Photos with human faces will always carry more visual weight.</li>
            <li>Movement: Moving objects naturally attract our attention and suggest to visitors where they should look next.</li>
            </ol>
