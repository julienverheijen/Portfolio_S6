            <h2>RESULTS</h2>
            <p class="results_text" id="deliverable-results-text"></p>
            <p>The following questions must be answered first, to answer the research question ("How do I make a portfolio visually attractive?").</p>
            <ul>
            <li>What are the latest design trends?</li>
            <li>How am I going to display all objects?</li>
            <li>Which colour pallet suits the best?</li>
            <li>Which fonts am I going to use?</li>
            <li>What will the navigation look like?</li>
            <li>Do the users know how to use to portfolio?</li>
            </ul>
            <p>&nbsp;</p>
            <p><strong>What are the latest design trends?</strong></p>
            <p>In an article from Krivec (2023) the best portfolio design trends of 2023 are discussed. I selected 5 trends that suit my personality and personal preferences and that I therefore want to reflect in the design.</p>
            <ol>
            <li>Curated Portfolio Home Page</li>
            </ol>
            <p>To create a good user experience, I want the user to get to the part he is looking for as quickly as possible (without clicking too much). By immediately showing the projects and clear navigation on the landing page.</p>
            <ol start="3">
            <li>Minimalism</li>
            </ol>
            <p>By keeping the portfolio minimalistic, the user can more easily find what he needs because there is little distraction from other elements, so the user only looks at what is needed. This also fits my personal style of "less is more".</p>
            <ol start="6">
            <li>Dark look</li>
            </ol>
            <p>Dark look is getting more and more popular among websites and apps. Another superb benefit of the dark look is an added layer of elegance and sophistication, with an immersive and &ldquo;atmospheric&rdquo; experience. By integrating a dark/light mode switcher ensures users to switch them between the mode they prefer.</p>
            <ol start="12">
            <li>Interactive and Animated</li>
            </ol>
            <p>Animations ensure that certain parts receive attention and create a creative display. Animations can improve the user experience, but if you use them too much, it can quickly become chaotic.&nbsp;</p>
            <ol start="18">
            <li>Grid Layout with Hover Effects</li>
            </ol>
            <p>The combination of grid layout and hover effects adds a touch of creativity and interactivity that can elevate your portfolio&rsquo;s visual impact and overall UX. By implementing hover effects to your portfolio grid, you invite your visitors to link with your portfolio more dynamically.</p>
            <p><strong>&nbsp;</strong></p>
