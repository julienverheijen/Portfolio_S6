            <p><strong>Do the users know how to use to portfolio? </strong></p>
            <p>I created a <a href="https://www.figma.com/proto/6j7ZiJSrYiKEJu5gb6VqdY/Untitled?page-id=0%3A1&type=design&node-id=33-557&viewport=249%2C178%2C0.14&t=seO5s0qzItPvoNpX-1&scaling=min-zoom&starting-point-node-id=33%3A557&show-proto-sidebar=1&mode=design" target="_blank">high-fidelity prototype</a> to find out how real users navigate through the portfolio.</p>
            <p>The prototype was tested with a usability test using the Maze tool. Users had to indicate what they saw first and were given different tasks to navigate through the portfolio.
            <p>Most important results:</p>
            <ol>
            <li>As also mentioned in Babich's article (2021), images with human faces have a heavy visual weight, just like saturated colours. On the landing page, the decision was made to use shades of grey to make the "all projects" button stand out because of the bright orange colour. The results of the usability test show that the image with the face still attracts more attention</li>
            </ol>
            <ol start="2">
            <li>When navigating to the right pages, you can see that every user can find the right page. What is noticeable is that not all testers have read the task properly and skipped a step. Also, some pages can be reached in two different ways. from the results of the test, we see that just over half use the 'all projects' page.</li>
            </ol>
            <ol start="3">
            <li>Most users found it easy to navigate through the design. However, there was one tester who did have difficulty with it.<strong>&nbsp;</strong></li>
            </ol>
